BEGIN;
CREATE TABLE muscle (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE
);

CREATE TABLE focus (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE
);

CREATE TABLE equipment (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE
);

CREATE TABLE force (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE
);

CREATE TABLE experience (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE
);

CREATE TABLE exercise (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name varchar(50) NOT NULL UNIQUE,
  target BIGINT NOT NULL REFERENCES muscle(id) ON DELETE CASCADE,
  focus BIGINT NOT NULL REFERENCES focus(id) ON DELETE CASCADE,
  equipment BIGINT NOT NULL REFERENCES equipment(id) ON DELETE CASCADE,
  force BIGINT NOT NULL REFERENCES force(id) ON DELETE CASCADE,
  experience BIGINT NOT NULL REFERENCES experience(id) ON DELETE CASCADE
);

CREATE TABLE workout_secondary (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  exercise BIGINT NOT NULL REFERENCES exercise(id) ON DELETE CASCADE,
  muscle BIGINT NOT NULL REFERENCES muscle(id) ON DELETE CASCADE
);
COMMIT;
